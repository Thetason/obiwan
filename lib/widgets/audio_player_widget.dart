class WaveformPainter extends CustomPainter {
  final List<double> audioData;
  final double currentPosition;

  WaveformPainter({
    required this.audioData,
    required this.currentPosition,
  });

  @override
  void paint(Canvas canvas, Size size) {
    if (audioData.isEmpty) {
      print('üìä [WaveformPainter] Ïò§ÎîîÏò§ Îç∞Ïù¥ÌÑ∞Í∞Ä ÎπÑÏñ¥ÏûàÏùå');
      return;
    }
    
    print('üìä [WaveformPainter] Ïò§ÎîîÏò§ Îç∞Ïù¥ÌÑ∞ Í∏∏Ïù¥: ${audioData.length}, Ï∫îÎ≤ÑÏä§ ÌÅ¨Í∏∞: ${size.width}x${size.height}');

    final paint = Paint()
      ..strokeWidth = 2.0
      ..strokeCap = StrokeCap.round;

    final centerY = size.height / 2;
    const int numBars = 150; // ÌôîÎ©¥Ïóê ÌëúÏãúÌï† Î∞îÏùò Í∞úÏàò
    final double barWidth = size.width / numBars;
    final int samplesPerBar = (audioData.length / numBars).ceil();
    
    print('üìä [WaveformPainter] Î∞î Í∞úÏàò: $numBars, Î∞îÎãπ ÏÉòÌîå: $samplesPerBar');

    // Ïò§ÎîîÏò§ Îç∞Ïù¥ÌÑ∞Î•º Îã§Ïö¥ÏÉòÌîåÎßÅÌïòÏó¨ Ïõ®Ïù¥Î∏åÌèº ÏÉùÏÑ±
    for (int i = 0; i < numBars; i++) {
      final x = i * barWidth + barWidth / 2;
      
      // Ìï¥Îãπ Íµ¨Í∞ÑÏùò RMS Í∞í Í≥ÑÏÇ∞
      double rms = 0.0;
      int startIdx = i * samplesPerBar;
      int endIdx = math.min(startIdx + samplesPerBar, audioData.length);
      
      if (startIdx < audioData.length) {
        double sum = 0.0;
        for (int j = startIdx; j < endIdx; j++) {
          sum += audioData[j] * audioData[j];
        }
        rms = math.sqrt(sum / (endIdx - startIdx));
        
        // RMS Í∞íÏùÑ Ï¶ùÌè≠ÌïòÏó¨ Îçî Ïûò Î≥¥Ïù¥Í≤å Ìï®
        rms = (rms * 10.0).clamp(0.0, 1.0);
      }
      
      final barHeight = rms * (size.height * 0.9); // ÎÜíÏù¥ Ï¶ùÍ∞Ä
      
      // Ïû¨ÏÉù ÏúÑÏπòÏóê Îî∞Î•∏ ÏÉâÏÉÅ Î≥ÄÍ≤Ω
      final isPlayed = (i / numBars) <= currentPosition;
      paint.color = isPlayed ? Color(0xFF007AFF) : Colors.grey[400]!;

      // Î∞î Í∑∏Î¶¨Í∏∞ (ÏµúÏÜå ÎÜíÏù¥ Î≥¥Ïû•)
      final minHeight = 2.0;
      final actualHeight = math.max(barHeight, minHeight);
      
      canvas.drawLine(
        Offset(x, centerY - actualHeight / 2),
        Offset(x, centerY + actualHeight / 2),
        paint,
      );
    }

    // ÌòÑÏû¨ ÏúÑÏπò Ïù∏ÎîîÏºÄÏù¥ÌÑ∞ (iOS Ïä§ÌÉÄÏùº)
    final indicatorX = size.width * currentPosition;
    final indicatorPaint = Paint()
      ..color = Color(0xFF007AFF)
      ..strokeWidth = 3;

    canvas.drawLine(
      Offset(indicatorX, 0),
      Offset(indicatorX, size.height),
      indicatorPaint,
    );
    
    // Ïù∏ÎîîÏºÄÏù¥ÌÑ∞ ÏÉÅÎã®Ïóê ÏûëÏùÄ Ïõê Í∑∏Î¶¨Í∏∞
    canvas.drawCircle(
      Offset(indicatorX, 0),
      4,
      Paint()..color = Color(0xFF007AFF),
    );
  }

  @override
  bool shouldRepaint(covariant WaveformPainter oldDelegate) {
    return currentPosition != oldDelegate.currentPosition ||
           audioData != oldDelegate.audioData;
  }
}