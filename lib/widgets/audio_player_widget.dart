        print('üîä [AudioPlayer] Ïã§Ï†ú Ïò§ÎîîÏò§ Ïû¨ÏÉù ÏãúÏûë');
        _startProgressTracking();
      } else {
        print('‚ùå [AudioPlayer] Ïò§ÎîîÏò§ Ïû¨ÏÉù Ïã§Ìå®');
        setState(() {
          _isPlaying = false;
        });
        _playController.reverse();
      }
    } else {
      _playController.reverse();
      widget.onPause?.call();
      
      // Ïã§Ï†ú ÎÑ§Ïù¥Ìã∞Î∏å Ïò§ÎîîÏò§ ÏùºÏãúÏ†ïÏßÄ
      await _nativeAudioService.pauseAudio();
      print('‚è∏Ô∏è [AudioPlayer] Ïã§Ï†ú Ïò§ÎîîÏò§ ÏùºÏãúÏ†ïÏßÄ');
    }
  }

  void _startProgressTracking() {
    // Ïã§Ï†ú Ïû¨ÏÉù ÏãúÍ∞ÑÏóê Îî∞Î•∏ ÏßÑÌñâÎ∞î ÏóÖÎç∞Ïù¥Ìä∏
