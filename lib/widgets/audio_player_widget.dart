  void _togglePlayPause() async {
    setState(() {
      _isPlaying = !_isPlaying;
    });
    
    if (_isPlaying) {
      _playController.forward();
      widget.onPlay?.call();
      
      // Ïã§Ï†ú ÎÑ§Ïù¥Ìã∞Î∏å Ïò§ÎîîÏò§ Ïû¨ÏÉù
